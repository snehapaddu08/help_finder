function showNearestMedical() {
    const nearestMedical = document.getElementById("nearestMedical");
    nearestMedical.style.display = nearestMedical.style.display === "none" ? "block" : "none";
  }
  
  function toggleHiddenFacilities() {
    const hiddenMedical = document.getElementById("hiddenMedical");
    hiddenMedical.style.display = hiddenMedical.style.display === "none" ? "block" : "none";
  }