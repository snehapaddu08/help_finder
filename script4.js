function showNearestFood() {
    const nearestFood = document.getElementById("nearestFood");
    nearestFood.style.display = nearestFood.style.display === "none" ? "block" : "none";
  }
  
  function toggleHiddenFood() {
    const hiddenFood = document.getElementById("hiddenFood");
    hiddenFood.style.display = hiddenFood.style.display === "none" ? "block" : "none";
  }